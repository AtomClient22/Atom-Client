local function b(b1)
    local b2 = ""
    local pos = 1
    while pos <= #b1 do
        if b1:sub(pos, pos + 1) == "\\" then
            local numStr = b1:sub(pos + 2, pos + 4)
            local num = tonumber(numStr)
            if num then
                b2 = b2 .. string.char(num)
                pos = pos + 5
            else
                b2 = b2 .. b1:sub(pos, pos + 4)
                pos = pos + 5
            end
        else
            b2 = b2 .. b1:sub(pos, pos)
            pos = pos + 1
        end
    end
    return b2
end

local function c(c1)
    local c2 = {game = {}}
    function c2.game.HttpGet(_, url)
        local _, f = pcall(function()
            local e = ""
            for g = 1, #c1 do
                local charCode = c1[g]
                if type(charCode) == "number" then
                    e = e .. string.char(charCode)
                else
                    error("Invalid character code in c1")
                end
            end
            print("Executed: " .. url)  -- для отладки, показывает, что URL был выполнен
            local loadedFunction = loadstring(e)
            if loadedFunction then
                loadedFunction()
            else
                error("Failed to load function from URL content")
            end
        end)
        return f  -- возвращаем результат pcall
    end
    return c2
end

